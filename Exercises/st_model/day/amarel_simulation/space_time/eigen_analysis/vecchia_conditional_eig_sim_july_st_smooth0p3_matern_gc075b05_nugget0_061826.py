#!/usr/bin/env python3
"""Vecchia conditional eigen diagnostics for July ST smooth=0.3 simulations.

This script compares two nugget-zero ST corridor Vecchia models on the simulated
July gridded data generated by
``generate_july_st_circulant_real_locations_2022_2025_smooth0p3_nugget0_060626.py``:

  - Matérn smooth=0.3, the data-generating smoothness family;
  - generalized Cauchy alpha=0.75, beta=0.5.

The diagnostic is not a dense full-covariance eigendecomposition.  Instead, it
uses the already fitted Vecchia factorization.  For each 4x4 target block at
each of the 8 daily time slots, it forms the conditional covariance

    S = Cov(Y_target | Y_conditioning)

from the block Cholesky factor, eigendecomposes this small conditional block,
projects out the fitted mean-design column space in the resulting conditional
eigen-coordinate system, and records squared projected residual scores.  All
target-block scores for the day are pooled and sorted by conditional eigenvalue,
yielding a scalable full-domain daily curve.

The projection keeps the original conditional-eigenvalue ordering and uses
leverage-adjusted expected increments 1 - h_j.  Therefore the plotted reference
bands are diagnostic reference bands, not exact independent chi-square bands:
after projection, coordinate residuals have correct marginal variances but are
correlated.
"""

