def clean_json_value(value: Any) -> Any:
    if isinstance(value, Path):
        return str(value)
    if isinstance(value, (np.floating, np.integer)):
        return value.item()
    if isinstance(value, np.ndarray):
        return value.tolist()
    if isinstance(value, torch.Tensor):
        if value.numel() == 1:
            return float(value.detach().cpu().item())
        return value.detach().cpu().tolist()
    if isinstance(value, (list, tuple)):
        return [clean_json_value(v) for v in value]
    if isinstance(value, dict):
        return {str(k): clean_json_value(v) for k, v in value.items()}
    return value


def parse_tokens(values: Iterable[str]) -> list[str]:
    out: list[str] = []
    for value in values:
        out.extend(part.strip() for part in str(value).split(",") if part.strip())
    return out


def parse_int_tokens(values: Iterable[str]) -> list[int]:
    return [int(v) for v in parse_tokens(values)]


def parse_day_idxs(text: str, n_keys: int | None = None, hours_per_day: int = 8) -> list[int]:
    text = str(text).strip().lower()
    if text == "all":
        if n_keys is None:
            raise ValueError("--days all requires n_keys")
        return list(range(int(n_keys) // int(hours_per_day)))
    parts = [p.strip() for p in text.split(",") if p.strip()]
    if len(parts) == 2:
        start, end = int(parts[0]), int(parts[1])
        return list(range(start, end))
    return [int(p) for p in parts]


def parse_pair(text: str, cast=float) -> list[Any]:
    parts = [p.strip() for p in str(text).split(",") if p.strip()]
    if len(parts) != 2:
        raise argparse.ArgumentTypeError(f"Expected two comma-separated values, got {text!r}")
    return [cast(parts[0]), cast(parts[1])]


def parse_gems_hour_key(key: str) -> pd.Timestamp | None:
    pat = r"^y(?P<yy>\d{2})m(?P<mm>\d{2})day(?P<dd>\d{2})_hm(?P<hh>\d{2}):(?P<minute>\d{2})$"
    match = re.match(pat, str(key))
    if not match:
        return None
    parts = {name: int(value) for name, value in match.groupdict().items()}
    return pd.Timestamp(
        year=2000 + parts["yy"],
        month=parts["mm"],
        day=parts["dd"],
        hour=parts["hh"],
        minute=parts["minute"],
        tz="UTC",
    )


def sorted_hour_keys(obj: dict[str, pd.DataFrame]) -> list[str]:
    parsed = []
    fallback = []
    for key in obj:
        ts = parse_gems_hour_key(str(key))
        if ts is None:
            fallback.append(str(key))
        else:
            parsed.append((ts, str(key)))
    if parsed:
        return [key for _, key in sorted(parsed)]
    return sorted(fallback)


