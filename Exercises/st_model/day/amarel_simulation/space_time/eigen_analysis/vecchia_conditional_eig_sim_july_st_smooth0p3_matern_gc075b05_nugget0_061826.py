def main() -> None:
    args = build_arg_parser().parse_args()
    model_variants = parse_tokens(args.model_variants)
    for variant in model_variants:
        if variant not in MODEL_SPECS:
            raise ValueError(f"Unknown model variant {variant!r}. Known: {sorted(MODEL_SPECS)}")
    device = resolve_device(args)
    out_dir = args.out_dir or default_output_root()
    out_dir.mkdir(parents=True, exist_ok=True)

    run_config = {
        "created": datetime.now().isoformat(timespec="seconds"),
        "script": str(Path(__file__).resolve()),
        "src": str(SRC),
        "device": str(device),
        "args": clean_json_value(vars(args)),
        "model_specs": clean_json_value(MODEL_SPECS),
        "true_init_physical": TRUE_INIT_PHYSICAL,
        "diagnostic_definition": (
            "Vecchia conditional target-block covariance eigenbasis; conditional-eigen scores are projected "
            "off the fitted mean-design column space via P=I-Z(Z'Z)^-1Z'; curves are sorted by conditional eigenvalue "
            "and scaled by cumulative projected expected df sum(1-h_j)"
        ),
        "gpu_optimization": "Conditional scores, projection/leverage, sorting, and cumulative sums stay on the torch device until final CSV/plot materialization.",
        "loss_definition": "Vecchia objective per target observation returned by model, matching total conditional NLL divided by conditional score count",
    }
    (out_dir / "run_config.json").write_text(json.dumps(clean_json_value(run_config), indent=2, sort_keys=True), encoding="utf-8")

    print("SRC:", SRC, flush=True)
    print("device:", device, flush=True)
    print("out_dir:", out_dir, flush=True)
    print("model_variants:", model_variants, flush=True)

    assets = load_day_assets(args)
    summary_rows: list[dict[str, Any]] = []
    avg_rows: list[pd.DataFrame] = []

    for asset in assets:
        print("\n" + "=" * 100, flush=True)
        print(
            f"Day year={asset.year} day_idx={asset.day_idx} day={asset.day_label} "
            f"rows/time={asset.n_rows_per_time:,} valid={asset.n_valid:,}/{asset.n_total:,}",
            flush=True,
        )
        print("=" * 100, flush=True)
        day_curves: dict[str, pd.DataFrame] = {}
        day_summary_rows: list[dict[str, Any]] = []

        for model_variant in model_variants:
            print(f"\n--- Fitting and diagnosing {model_variant} ---", flush=True)
            model = None
            try:
                fit_row, model, beta = fit_one_model(asset, model_variant, device, args)
                params = torch.as_tensor(
                    physical_to_log_phi(
                        {
                            "sigmasq": fit_row["est_sigmasq"],
                            "range_lat": fit_row["est_range_lat"],
                            "range_lon": fit_row["est_range_lon"],
                            "range_time": fit_row["est_range_time"],
                            "advec_lat": fit_row["est_advec_lat"],
                            "advec_lon": fit_row["est_advec_lon"],
                            "nugget": 0.0,
                        }
                    ),
                    device=device,
                    dtype=DTYPE,
                )
                t_diag = time.time()
                curve, diag_summary = conditional_eigen_curve(model, params, beta, args)
                diag_s = time.time() - t_diag
                row = {
                    **fit_row,
                    **diag_summary,
                    "diag_s": float(diag_s),
                }
                print(
                    pd.Series(
                        {
                            "model": row["model_label"],
                            "loss/obs": f"{row['vecchia_loss_per_obs']:.{LOSS_DECIMALS}f}",
                            "conditional_loss/score": f"{row['conditional_loss_per_score']:.{LOSS_DECIMALS}f}",
                            "mean_y2": f"{row['mean_y2']:.5f}",
                            "D": f"{row['max_abs_bridge_scaled']:.5f}",
                            "n_scores": row["n_conditional_scores"],
                            "diag_s": f"{diag_s:.1f}",
                        }
                    ).to_string(),
                    flush=True,
                )
                if args.save_daily_curves:
                    curve_path = out_dir / "daily_curves" / f"year_{asset.year}" / f"sim_{asset.year}_day{asset.day_idx + 1:02d}_{model_variant}_conditional_eig_curve.csv"
                    curve_path.parent.mkdir(parents=True, exist_ok=True)
                    curve.round(ROUND_DECIMALS).to_csv(curve_path, index=False, float_format=f"%.{ROUND_DECIMALS}f")
                day_curves[model_variant] = curve
                avg_rows.append(
                    resample_curve(curve, int(args.resample_grid)).assign(
                        year=int(asset.year),
                        month=int(asset.month),
                        day_idx=int(asset.day_idx),
                        day=str(asset.day_label),
                        model_variant=str(model_variant),
                    )
                )
            except Exception as exc:
                row = {
                    "status": "error",
                    "error": f"{type(exc).__name__}: {exc}",
                    "traceback": traceback.format_exc(limit=12),
                    "year": int(asset.year),
                    "month": int(asset.month),
                    "day_idx": int(asset.day_idx),
                    "day": str(asset.day_label),
                    "model_variant": str(model_variant),
                }
                print(f"ERROR for {model_variant}: {row['error']}", flush=True)
                traceback.print_exc()
            finally:
                if model is not None:
                    del model
                gc.collect()
                if device.type == "cuda":
                    torch.cuda.empty_cache()
            summary_rows.append(clean_json_value(row))
            day_summary_rows.append(clean_json_value(row))
            save_summary(summary_rows, out_dir)

        if day_curves:
            plot_daily_comparison(
                day_curves,
                day_summary_rows,
                out_dir / "daily_plots" / f"year_{asset.year}" / f"sim_{asset.year}_day{asset.day_idx + 1:02d}_vecchia_conditional_eig_comparison.png",
                f"Sim July {asset.year} day_idx={asset.day_idx} ({asset.day_label}): Vecchia conditional eigen diagnostic",
            )

        # Keep monthly plots current after each completed day.
        for year in sorted({int(r.get("year")) for r in summary_rows if r.get("status") == "ok"}):
            year_avg = [df for df in avg_rows if not df.empty and int(df["year"].iloc[0]) == year]
            year_summary = [r for r in summary_rows if r.get("status") == "ok" and int(r.get("year")) == year]
            write_monthly_outputs(year_avg, year_summary, out_dir, year)

    summary = save_summary(summary_rows, out_dir)
    print("\nDone.", flush=True)
    print("summary:", out_dir / f"{RUN_STEM}_summary.csv", flush=True)
    print("ok rows:", int((summary["status"] == "ok").sum()) if not summary.empty and "status" in summary.columns else 0, flush=True)


if __name__ == "__main__":
    main()
