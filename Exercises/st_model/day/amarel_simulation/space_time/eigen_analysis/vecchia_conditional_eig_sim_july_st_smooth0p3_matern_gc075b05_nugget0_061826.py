def resolve_device(args: argparse.Namespace) -> torch.device:
    if args.device is not None:
        device = torch.device(args.device)
        if device.type == "cuda" and not torch.cuda.is_available():
            raise RuntimeError(f"Requested {device}, but CUDA is not available.")
        return device
    if torch.cuda.is_available():
        return torch.device("cuda")
    if args.cuda_fallback == "error" or args.require_cuda:
        raise RuntimeError("CUDA is not available and CUDA fallback is disabled.")
    return torch.device("cpu")


def count_valid(source_map: dict[str, torch.Tensor]) -> tuple[int, int, dict[str, int]]:
    n_valid = 0
    n_total = 0
    valid_by_t: dict[str, int] = {}
    for key, tensor in source_map.items():
        valid = (~torch.isnan(tensor[:, 2])) & (~torch.isnan(tensor[:, 0])) & (~torch.isnan(tensor[:, 1]))
        count = int(valid.sum().item())
        n_valid += count
        n_total += int(tensor.shape[0])
        valid_by_t[str(key)] = count
    return n_valid, n_total, valid_by_t


def assert_grid_order_consistent(df_map: dict[str, pd.DataFrame], keys: list[str], base_coords: np.ndarray) -> None:
    for key in keys:
        coords = df_map[key][["Latitude", "Longitude"]].to_numpy(dtype=np.float64)
        if coords.shape != base_coords.shape or not np.allclose(coords, base_coords, equal_nan=True):
            raise RuntimeError(f"Regular grid coordinate order differs at {key}.")


def build_hour_tensor(
    df: pd.DataFrame,
    t_idx: int,
    monthly_mean: float,
    keep_exact_loc: bool,
    dtype: torch.dtype,
) -> torch.Tensor:
    tmp = df.copy()
    value = pd.to_numeric(tmp["ColumnAmountO3"], errors="coerce").to_numpy(dtype=np.float64)
    centered = value - float(monthly_mean)

    if keep_exact_loc and {"Source_Latitude", "Source_Longitude"}.issubset(tmp.columns):
        lat = pd.to_numeric(tmp["Source_Latitude"], errors="coerce").to_numpy(dtype=np.float64)
        lon = pd.to_numeric(tmp["Source_Longitude"], errors="coerce").to_numpy(dtype=np.float64)
        grid_lat = pd.to_numeric(tmp["Latitude"], errors="coerce").to_numpy(dtype=np.float64)
        grid_lon = pd.to_numeric(tmp["Longitude"], errors="coerce").to_numpy(dtype=np.float64)
        lat = np.where(np.isfinite(lat), lat, grid_lat)
        lon = np.where(np.isfinite(lon), lon, grid_lon)
    else:
        lat = pd.to_numeric(tmp["Latitude"], errors="coerce").to_numpy(dtype=np.float64)
        lon = pd.to_numeric(tmp["Longitude"], errors="coerce").to_numpy(dtype=np.float64)

    base = np.zeros((len(tmp), 4), dtype=np.float64)
    base[:, 0] = lat
    base[:, 1] = lon
    base[:, 2] = centered
    base[:, 3] = float(t_idx)
    base_tensor = torch.from_numpy(base).to(dtype=dtype)
    dummies = F.one_hot(torch.tensor([int(t_idx)], dtype=torch.long), num_classes=8).repeat(len(base_tensor), 1)
    dummies = dummies[:, 1:].to(dtype=dtype)
    return torch.cat([base_tensor, dummies], dim=1).contiguous()


def load_day_assets(args: argparse.Namespace) -> list[DayAsset]:
    data_root = args.data_root or default_data_root()
    years = parse_int_tokens(args.years)
    month = int(args.month)
    lat_range = parse_pair(args.lat_range, float)
    lon_range = parse_pair(args.lon_range, float)
    assets: list[DayAsset] = []

    for year in years:
        path = simulated_pickle_path(data_root, int(year), str(args.sim_kind))
        if not path.exists():
            raise FileNotFoundError(
                f"Simulated {args.sim_kind} pickle not found: {path}. "
                "For this ST Vecchia diagnostic, use the gridded pickle unless you have equal-length real-location maps."
            )
        obj = pd.read_pickle(path)
        if not isinstance(obj, dict):
            raise TypeError(f"Expected dict pickle at {path}, got {type(obj)}")
        key_idx = sorted_hour_keys(obj)
        day_idxs = parse_day_idxs(args.days, n_keys=len(key_idx), hours_per_day=int(args.hours_per_day))

        df_map: dict[str, pd.DataFrame] = {}
        for key in key_idx:
            df = obj[key].copy()
            mask = (
                (pd.to_numeric(df["Latitude"], errors="coerce") >= lat_range[0])
                & (pd.to_numeric(df["Latitude"], errors="coerce") <= lat_range[1])
                & (pd.to_numeric(df["Longitude"], errors="coerce") >= lon_range[0])
                & (pd.to_numeric(df["Longitude"], errors="coerce") <= lon_range[1])
            )
            df_map[key] = df[mask].reset_index(drop=True)

        ozone_vals = [
            pd.to_numeric(df["ColumnAmountO3"], errors="coerce").to_numpy(dtype=np.float64)
            for df in df_map.values()
        ]
        monthly_mean = float(np.nanmean(np.concatenate(ozone_vals))) if ozone_vals else 0.0
        base_coords = df_map[key_idx[0]][["Latitude", "Longitude"]].to_numpy(dtype=np.float64)
        print(
            f"\nLoaded {path}: year={year}, keys={len(key_idx)}, grid={base_coords.shape}, "
            f"monthly_mean={monthly_mean:.6f}",
            flush=True,
        )

        for day_idx in day_idxs:
            start, end = int(day_idx) * int(args.hours_per_day), (int(day_idx) + 1) * int(args.hours_per_day)
            selected_keys = key_idx[start:end]
            if len(selected_keys) != int(args.hours_per_day):
                print(f"Skipping year={year} day_idx={day_idx}: expected {args.hours_per_day} keys, got {len(selected_keys)}", flush=True)
                continue
            assert_grid_order_consistent(df_map, selected_keys, base_coords)
            source_map = {
                key: build_hour_tensor(
                    df_map[key],
                    t_idx=i,
                    monthly_mean=monthly_mean,
                    keep_exact_loc=bool(args.keep_exact_loc),
                    dtype=DTYPE,
                )
                for i, key in enumerate(selected_keys)
            }
            n_valid, n_total, _ = count_valid(source_map)
            ts = parse_gems_hour_key(selected_keys[0])
            day_label = ts.strftime("%Y-%m-%d") if ts is not None else f"{year}-{month:02d}-day{int(day_idx) + 1:02d}"
            assets.append(
                DayAsset(
                    year=int(year),
                    month=month,
                    day_idx=int(day_idx),
                    day_label=day_label,
                    keys=list(selected_keys),
                    source_map={k: v.contiguous() for k, v in source_map.items()},
                    grid_coords_np=base_coords.copy(),
                    monthly_mean=monthly_mean,
                    n_rows_per_time=int(base_coords.shape[0]),
                    n_valid=int(n_valid),
                    n_total=int(n_total),
                )
            )
    return assets


def physical_to_log_phi(params: dict[str, float]) -> list[float]:
    sigmasq = float(params["sigmasq"])
    range_lat = float(params["range_lat"])
    range_lon = float(params["range_lon"])
    range_time = float(params["range_time"])
    phi2 = 1.0 / range_lon
    phi1 = sigmasq * phi2
    phi3 = (range_lon / range_lat) ** 2
    phi4 = (range_lon / range_time) ** 2
    return [
        float(np.log(phi1)),
        float(np.log(phi2)),
        float(np.log(phi3)),
        float(np.log(phi4)),
        float(params["advec_lat"]),
        float(params["advec_lon"]),
    ]


def make_params_list(init_physical: dict[str, float], dtype: torch.dtype, device: torch.device) -> list[Parameter]:
    return [
        Parameter(torch.tensor(val, dtype=dtype, device=device))
        for val in physical_to_log_phi(init_physical)
    ]


def backmap_params(raw: list[float]) -> dict[str, float]:
    vals = [float(x) for x in raw]
    phi1, phi2, phi3, phi4 = np.exp(vals[0]), np.exp(vals[1]), np.exp(vals[2]), np.exp(vals[3])
    range_lon = 1.0 / phi2
    return {
        "sigmasq": float(phi1 / phi2),
        "range_lat": float(range_lon / np.sqrt(phi3)),
        "range_lon": float(range_lon),
        "range_time": float(range_lon / np.sqrt(phi4)),
        "advec_lat": float(vals[4]),
        "advec_lon": float(vals[5]),
        "nugget": 0.0,
    }


def build_model(spec: dict[str, Any], source_map: dict[str, torch.Tensor], grid_coords_np: np.ndarray, args: argparse.Namespace):
    if spec["family"] == "matern":
        return RealDataCorridorWidth4x4Lag643NoNuggetSplineFit(
            smooth=float(spec["smooth"]),
            input_map=source_map,
            grid_coords=grid_coords_np,
            lag1_lon_offset=float(args.real_reference_advec_lon_abs),
            daily_stride=int(args.daily_stride),
            target_chunk_size=int(args.target_chunk_size),
            min_target_points=int(args.min_target_points),
            spline_n_points=int(args.spline_n_points),
            spline_r_max=float(args.spline_r_max),
        )
    if spec["family"] == "cauchy":
        return RealDataCorridorWidth4x4Lag643NoNuggetGeneralizedCauchyFit(
            gc_alpha=float(spec["gc_alpha"]),
            gc_beta=float(spec["gc_beta"]),
            input_map=source_map,
            grid_coords=grid_coords_np,
            lag1_lon_offset=float(args.real_reference_advec_lon_abs),
            daily_stride=int(args.daily_stride),
            target_chunk_size=int(args.target_chunk_size),
            min_target_points=int(args.min_target_points),
        )
    raise ValueError(f"Unknown model family {spec['family']!r}")


def fit_one_model(asset: DayAsset, model_variant: str, device: torch.device, args: argparse.Namespace) -> tuple[dict[str, Any], Any, torch.Tensor]:
    spec = MODEL_SPECS[model_variant]
    source_map = {
        k: v.to(device=device, dtype=DTYPE, non_blocking=True).contiguous()
        for k, v in asset.source_map.items()
    }
    model = build_model(spec, source_map, asset.grid_coords_np, args)
    params_list = make_params_list(TRUE_INIT_PHYSICAL, dtype=DTYPE, device=device)

    t0 = time.time()
    model.precompute_conditioning_sets()
    precompute_s = time.time() - t0

    optimizer = model.set_optimizer(
        params_list,
        lr=float(args.lbfgs_lr),
        max_iter=int(args.lbfgs_eval),
        max_eval=int(args.lbfgs_eval),
        history_size=int(args.lbfgs_history),
    )

    t1 = time.time()
    if args.suppress_fit_prints:
        with contextlib.redirect_stdout(io.StringIO()):
            out, steps_ran = model.fit_vecc_lbfgs(
                params_list,
                optimizer,
                max_steps=int(args.lbfgs_steps),
                grad_tol=float(args.grad_tol),
            )
    else:
        out, steps_ran = model.fit_vecc_lbfgs(
            params_list,
            optimizer,
            max_steps=int(args.lbfgs_steps),
            grad_tol=float(args.grad_tol),
        )
    fit_s = time.time() - t1

    raw_params = [float(x) for x in out[:6]]
    params_tensor = torch.stack([p.detach().reshape(()) for p in params_list]).to(device=device, dtype=DTYPE)
    beta = model.get_gls_beta(params_tensor).detach()
    est = backmap_params(raw_params)
    cluster_summary = model.cluster_summary()
    fit_loss_per_obs = float(out[-1])
    row = {
        "status": "ok",
        "error": "",
        "year": int(asset.year),
        "month": int(asset.month),
        "day_idx": int(asset.day_idx),
        "day": str(asset.day_label),
        "model_variant": str(model_variant),
        "model_family": str(spec["family"]),
        "model_label": str(spec["label"]),
        "smooth": float(spec["smooth"]) if pd.notna(spec["smooth"]) else np.nan,
        "gc_alpha": float(spec["gc_alpha"]) if pd.notna(spec["gc_alpha"]) else np.nan,
        "gc_beta": float(spec["gc_beta"]) if pd.notna(spec["gc_beta"]) else np.nan,
        "nugget_mode": "zero",
        "n_rows_per_time": int(asset.n_rows_per_time),
        "n_valid_o3": int(asset.n_valid),
        "n_total_rows": int(asset.n_total),
        "valid_rate": float(asset.n_valid / asset.n_total) if asset.n_total else np.nan,
        "monthly_mean": float(asset.monthly_mean),
        "first_key": asset.keys[0],
        "last_key": asset.keys[-1],
        "spec_name": VECCHIA_SPEC_NAME,
        "block_shape": f"{BLOCK_SHAPE[0]}x{BLOCK_SHAPE[1]}",
        "lag_pattern": f"{LAG_COUNTS[0]}/{LAG_COUNTS[1]}/{LAG_COUNTS[2]}",
        "reference_advec_lon_abs": float(args.real_reference_advec_lon_abs),
        "vecchia_loss_per_obs": fit_loss_per_obs,
        "steps_raw": int(steps_ran),
        "precompute_s": float(precompute_s),
        "fit_s": float(fit_s),
        "total_fit_s": float(precompute_s + fit_s),
        **{f"est_{k}": float(est[k]) for k in ["sigmasq", "range_lat", "range_lon", "range_time", "advec_lat", "advec_lon", "nugget"]},
        **cluster_summary,
    }
    return row, model, beta


def conditional_eigen_curve(
    model: Any,
    params: torch.Tensor,
    beta: torch.Tensor,
    args: argparse.Namespace,
) -> tuple[pd.DataFrame, dict[str, Any]]:
    if not getattr(model, "is_precomputed", False):
        raise RuntimeError("Model must be precomputed before conditional_eigen_curve.")
    if not hasattr(model, "_cluster_batches"):
        raise RuntimeError("This diagnostic currently expects a cluster Vecchia model with _cluster_batches.")

    eigen_chunks: list[torch.Tensor] = []
    y_score_chunks: list[torch.Tensor] = []
    x_score_chunks: list[torch.Tensor] = []
    local_rank_chunks: list[torch.Tensor] = []
    log_det_half_total = params.new_tensor(0.0)
    n_blocks = 0
    n_scores = 0
    diag_chunk_size = int(args.diag_chunk_size) if int(args.diag_chunk_size) > 0 else int(args.target_chunk_size)
    diag_chunk_size = max(1, diag_chunk_size)

    with torch.no_grad():
        for batch in model._cluster_batches:
            if batch.X.shape[0] == 0:
                continue
            target_slice = slice(batch.max_cond_points, batch.max_cond_points + batch.target_size)
            target_size = int(batch.target_size)
            for start in range(0, batch.X.shape[0], diag_chunk_size):
                end = min(start + diag_chunk_size, batch.X.shape[0])
                cov = model.matern_cov_batched(params, batch.X[start:end])
                L = torch.linalg.cholesky(cov)
                Z_locs = torch.linalg.solve_triangular(L, batch.Locs[start:end], upper=False)
                Z_y = torch.linalg.solve_triangular(L, batch.Y[start:end], upper=False)
                u_X = Z_locs[:, target_slice, :]
                u_y = Z_y[:, target_slice, 0]

                L_tt = L[:, target_slice, target_slice]
                cond_y = torch.bmm(L_tt, u_y.unsqueeze(-1))
                cond_X = torch.bmm(L_tt, u_X)
                s_cond = torch.bmm(L_tt, L_tt.transpose(1, 2))
                evals, evecs = torch.linalg.eigh(0.5 * (s_cond + s_cond.transpose(1, 2)))
                evals = evals.clamp_min(EPS).flip(dims=[1])
                evecs = evecs.flip(dims=[2])
                sqrt_evals = torch.sqrt(evals).unsqueeze(-1)
                y_scores = torch.bmm(evecs.transpose(1, 2), cond_y).squeeze(-1) / sqrt_evals.squeeze(-1)
                x_scores = torch.bmm(evecs.transpose(1, 2), cond_X) / sqrt_evals

                log_diag = torch.log(torch.diagonal(L_tt, dim1=1, dim2=2))
                log_det_half_total = log_det_half_total + log_diag.sum()
                n_blocks += int(end - start)
                n_scores += int(y_scores.numel())

                eigen_chunks.append(evals.reshape(-1).detach())
                y_score_chunks.append(y_scores.reshape(-1).detach())
                x_score_chunks.append(x_scores.reshape(-1, x_scores.shape[-1]).detach())
                local_rank = torch.arange(1, target_size + 1, device=params.device, dtype=torch.int16).repeat(int(end - start))
                local_rank_chunks.append(local_rank)

                del cov, L, Z_locs, Z_y, u_X, u_y, L_tt, cond_y, cond_X, s_cond, evals, evecs, sqrt_evals, y_scores, x_scores

    if not eigen_chunks:
        raise RuntimeError("No conditional eigen scores were generated.")

    eigenvalues = torch.cat(eigen_chunks).to(dtype=DTYPE)
    y_scores = torch.cat(y_score_chunks).to(dtype=DTYPE)
    x_scores = torch.cat(x_score_chunks).to(dtype=DTYPE)
    local_rank = torch.cat(local_rank_chunks)
    finite = torch.isfinite(eigenvalues) & torch.isfinite(y_scores) & torch.isfinite(x_scores).all(dim=1) & (eigenvalues > 0.0)
    eigenvalues = eigenvalues[finite]
    y_scores = y_scores[finite]
    x_scores = x_scores[finite]
    local_rank = local_rank[finite]

    beta_vec = beta.detach().reshape(-1).to(device=x_scores.device, dtype=DTYPE)
    residual = y_scores - x_scores @ beta_vec
    xtx = x_scores.T @ x_scores
    xtx_jitter = torch.eye(xtx.shape[0], device=xtx.device, dtype=DTYPE) * 1e-6
    xtx_inv = torch.linalg.pinv(xtx + xtx_jitter)
    leverage = torch.einsum("ij,jk,ik->i", x_scores, xtx_inv, x_scores)
    leverage = leverage.clamp(0.0, 1.0 - 1e-10)
    expected_increment = 1.0 - leverage
    y2 = residual.pow(2)

    order = torch.argsort(eigenvalues, descending=True)
    eigenvalues = eigenvalues[order]
    y2 = y2[order]
    local_rank = local_rank[order]
    leverage = leverage[order]
    expected_increment = expected_increment[order]

    index = torch.arange(1, int(y2.numel()) + 1, device=y2.device, dtype=torch.int64)
    csum = torch.cumsum(y2, dim=0)
    expected_csum = torch.cumsum(expected_increment, dim=0)
    m = int(y2.numel())
    residual_df = float(expected_increment.sum().detach().cpu().item())
    width = float(args.brown_bridge_q) * math.sqrt(2.0 * max(residual_df, 1.0))
    bridge_d = (
        float(torch.max(torch.abs(csum - expected_csum)).detach().cpu().item() / math.sqrt(2.0 * max(residual_df, 1.0)))
        if m
        else np.nan
    )
    loss_total = float((log_det_half_total + 0.5 * y2.sum()).detach().cpu().item())

    index_np = index.detach().cpu().numpy()
    eigenvalues_np = eigenvalues.detach().cpu().numpy()
    local_rank_np = local_rank.detach().cpu().numpy().astype(np.int16, copy=False)
    leverage_np = leverage.detach().cpu().numpy()
    expected_increment_np = expected_increment.detach().cpu().numpy()
    y2_np = y2.detach().cpu().numpy()
    csum_np = csum.detach().cpu().numpy()
    expected_csum_np = expected_csum.detach().cpu().numpy()

    curve = pd.DataFrame(
        {
            "index": index_np,
            "frac_index": expected_csum_np / max(residual_df, EPS),
            "conditional_eigenvalue": eigenvalues_np,
            "local_eigen_rank": local_rank_np,
            "leverage": leverage_np,
            "expected_increment": expected_increment_np,
            "y2": y2_np,
            "cumsum_y2": csum_np,
            "scaled_cumsum": csum_np / max(residual_df, EPS),
            "expected": expected_csum_np,
            "scaled_expected": expected_csum_np / max(residual_df, EPS),
            "band_lower": expected_csum_np - width,
            "band_upper": expected_csum_np + width,
            "scaled_band_lower": expected_csum_np / max(residual_df, EPS) - width / max(residual_df, EPS),
            "scaled_band_upper": expected_csum_np / max(residual_df, EPS) + width / max(residual_df, EPS),
        }
    )
    rank_summary = (
        pd.DataFrame({"local_eigen_rank": local_rank_np, "y2": y2_np, "expected_increment": expected_increment_np})
        .groupby("local_eigen_rank", as_index=False)
        .agg(
            local_rank_sum_y2=("y2", "sum"),
            local_rank_expected=("expected_increment", "sum"),
            local_rank_n=("y2", "size"),
        )
    )
    summary = {
        "n_conditional_blocks": int(n_blocks),
        "n_conditional_scores": int(m),
        "n_scores_before_filter": int(n_scores),
        "residual_df": residual_df,
        "mean_leverage": float(leverage.mean().detach().cpu().item()) if m else np.nan,
        "max_leverage": float(leverage.max().detach().cpu().item()) if m else np.nan,
        "conditional_loss_total": float(loss_total),
        "conditional_loss_per_score": float(loss_total / max(m, 1)),
        "mean_y2": float((y2.sum() / max(residual_df, EPS)).detach().cpu().item()) if m else np.nan,
        "sum_y2": float(csum[-1].detach().cpu().item()) if m else np.nan,
        "max_abs_bridge_scaled": bridge_d,
        "min_conditional_eigenvalue": float(eigenvalues.min().detach().cpu().item()) if m else np.nan,
        "max_conditional_eigenvalue": float(eigenvalues.max().detach().cpu().item()) if m else np.nan,
    }
    for _, row in rank_summary.iterrows():
        rank = int(row["local_eigen_rank"])
        summary[f"local_rank{rank:02d}_mean_y2"] = float(row["local_rank_sum_y2"] / max(row["local_rank_expected"], EPS))
        summary[f"local_rank{rank:02d}_n"] = int(row["local_rank_n"])
    return curve, summary


def resample_curve(curve: pd.DataFrame, n_grid: int = 200) -> pd.DataFrame:
    if curve.empty:
        return pd.DataFrame()
    frac = curve["frac_index"].to_numpy(dtype=np.float64)
    scaled = curve["scaled_cumsum"].to_numpy(dtype=np.float64)
    grid = np.linspace(1.0 / int(n_grid), 1.0, int(n_grid))
    vals = np.interp(grid, frac, scaled)
    residual_df = float(curve["expected"].iloc[-1]) if "expected" in curve.columns and not curve.empty else float(len(curve))
    return pd.DataFrame({"frac_index": grid, "scaled_cumsum": vals, "n_scores": int(len(curve)), "residual_df": residual_df})


def model_label(model_variant: str, summary: pd.DataFrame | None = None) -> str:
    spec = MODEL_SPECS.get(model_variant, {})
    label = str(spec.get("label", model_variant))
    if summary is not None and not summary.empty and "vecchia_loss_per_obs" in summary.columns:
        vals = pd.to_numeric(
            summary.loc[summary["model_variant"] == model_variant, "vecchia_loss_per_obs"],
            errors="coerce",
        ).dropna()
        if not vals.empty:
            label += f" loss/obs={float(vals.mean()):.{LOSS_DECIMALS}f}"
    return label


def plot_daily_comparison(curves: dict[str, pd.DataFrame], summary_rows: list[dict[str, Any]], out_path: Path, title: str) -> None:
    if not curves:
        return
    summary = pd.DataFrame(summary_rows)
    fig, ax = plt.subplots(figsize=(6.4, 5.2))
    y_max = 1.05
    for model_variant, curve in curves.items():
        if curve.empty:
            continue
        color = MODEL_SPECS.get(model_variant, {}).get("color", None)
        x = curve["frac_index"].to_numpy(dtype=np.float64)
        y = curve["scaled_cumsum"].to_numpy(dtype=np.float64)
        y_max = max(y_max, float(np.nanmax(y)) * 1.04)
        ax.plot(x, y, color=color, linewidth=1.8, label=model_label(model_variant, summary))
    grid = np.linspace(0.0, 1.0, 200)
    ax.plot(grid, grid, color="0.45", linewidth=1.2)
    first_curve = next(iter(curves.values()))
    if not first_curve.empty:
        m_bar = max(float(first_curve["expected"].iloc[-1]) if "expected" in first_curve.columns else float(len(first_curve)), 1.0)
        band = BROWN_BRIDGE_Q95 * math.sqrt(2.0 / m_bar)
        ax.plot(grid, grid - band, color="0.65", linestyle=(0, (4, 4)), linewidth=0.9)
        ax.plot(grid, grid + band, color="0.65", linestyle=(0, (4, 4)), linewidth=0.9)
    ax.set_xlim(0, 1)
    ax.set_ylim(0, y_max)
    ax.set_xlabel("projected expected df fraction, sorted by conditional eigenvalue")
    ax.set_ylabel("projected cumulative squared score / residual df")
    ax.set_title(title)
    ax.grid(alpha=0.22)
    ax.legend(fontsize=8)
    fig.tight_layout()
    out_path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(out_path, dpi=180, bbox_inches="tight")
    plt.close(fig)


def write_monthly_outputs(avg_rows: list[pd.DataFrame], summary_rows: list[dict[str, Any]], out_dir: Path, year: int) -> None:
    if not avg_rows:
        return
    all_avg = pd.concat(avg_rows, ignore_index=True)
    summary = pd.DataFrame(summary_rows)
    all_avg.round(ROUND_DECIMALS).to_csv(
        out_dir / f"sim_{year}_vecchia_conditional_eig_daily_resampled_curves.csv",
        index=False,
        float_format=f"%.{ROUND_DECIMALS}f",
    )
    keys = ["model_variant", "frac_index"]
    monthly = (
        all_avg.groupby(keys, as_index=False)
        .agg(
            scaled_cumsum_mean=("scaled_cumsum", "mean"),
            scaled_cumsum_sd=("scaled_cumsum", "std"),
            n_days=("scaled_cumsum", "count"),
            n_scores_mean=("n_scores", "mean"),
            residual_df_mean=("residual_df", "mean"),
        )
        .sort_values(keys)
    )
    monthly.round(ROUND_DECIMALS).to_csv(
        out_dir / f"sim_{year}_vecchia_conditional_eig_monthly_average_curves.csv",
        index=False,
        float_format=f"%.{ROUND_DECIMALS}f",
    )

    fig, ax = plt.subplots(figsize=(6.6, 5.3))
    y_max = 1.05
    for model_variant, g in monthly.groupby("model_variant", sort=True):
        color = MODEL_SPECS.get(str(model_variant), {}).get("color", None)
        g = g.sort_values("frac_index")
        x = g["frac_index"].to_numpy(dtype=np.float64)
        y = g["scaled_cumsum_mean"].to_numpy(dtype=np.float64)
        sd = g["scaled_cumsum_sd"].fillna(0.0).to_numpy(dtype=np.float64)
        y_max = max(y_max, float(np.nanmax(y + sd)) * 1.04)
        ax.plot(x, y, color=color, linewidth=2.0, label=model_label(str(model_variant), summary))
        ax.fill_between(x, y - sd, y + sd, color=color, alpha=0.10, linewidth=0)
    grid = np.linspace(0.0, 1.0, 200)
    ax.plot(grid, grid, color="0.45", linewidth=1.2)
    residual_df_mean = pd.to_numeric(monthly["residual_df_mean"], errors="coerce").dropna()
    if not residual_df_mean.empty:
        band = BROWN_BRIDGE_Q95 * math.sqrt(2.0 / max(float(residual_df_mean.mean()), 1.0))
        ax.plot(grid, grid - band, color="0.65", linestyle=(0, (4, 4)), linewidth=0.9)
        ax.plot(grid, grid + band, color="0.65", linestyle=(0, (4, 4)), linewidth=0.9)
    ax.set_xlim(0, 1)
    ax.set_ylim(0, y_max)
    ax.set_xlabel("projected expected df fraction, sorted by conditional eigenvalue")
    ax.set_ylabel("monthly mean projected cumulative squared score / residual df")
    ax.set_title(f"Sim July {year}: monthly average Vecchia conditional eigen diagnostic")
    ax.grid(alpha=0.22)
    ax.legend(fontsize=8)
    fig.tight_layout()
    plot_dir = out_dir / "monthly_average_plots"
    plot_dir.mkdir(parents=True, exist_ok=True)
    fig.savefig(plot_dir / f"sim_{year}_monthly_average_vecchia_conditional_eig_comparison.png", dpi=180, bbox_inches="tight")
    plt.close(fig)


def save_summary(summary_rows: list[dict[str, Any]], out_dir: Path) -> pd.DataFrame:
    df = pd.DataFrame(summary_rows)
    if not df.empty:
        numeric_cols = df.select_dtypes(include=[np.number]).columns
        df[numeric_cols] = df[numeric_cols].round(ROUND_DECIMALS)
    df.to_csv(out_dir / f"{RUN_STEM}_summary.csv", index=False, float_format=f"%.{ROUND_DECIMALS}f")
    return df


