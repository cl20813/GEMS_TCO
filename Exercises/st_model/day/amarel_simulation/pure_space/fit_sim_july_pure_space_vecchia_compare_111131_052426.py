"""
Fit the default 4x4 B2 cluster isotropic Vecchia on pre-generated July
simulation assets.

Created 2026-05-24.

What this script does:
  1. Load the real-location-like July ST simulation pickles generated by
     simulate_data/generate_july_st_circulant_real_locations_2022_2025.py.
  2. Treat each day asset as eight independent pure-space replicates that share
     one isotropic Matérn covariance parameter set.
  3. Fit the 4x4 cluster max-min pure-space model from
     kernels_space_iso_cluster_052426.py with B=2 neighbor blocks by default.
  4. Append one compact row per fit and refresh running CSV/TXT summaries.

No new data are simulated here.  This is only a fitting/optimization comparison.

Truth convention:
  The simulation truth is anisotropic in latitude/longitude, while both tested
  modules are isotropic.  For scoring the fitted isotropic range, this script
  uses sqrt(range_lat * range_lon) as the reference isotropic range.
"""

