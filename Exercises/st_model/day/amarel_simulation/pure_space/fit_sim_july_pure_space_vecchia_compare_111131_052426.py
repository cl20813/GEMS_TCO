def main() -> None:
    parser = argparse.ArgumentParser()
    add_args(parser)
    args = parser.parse_args()

    if args.require_cuda and DEVICE.type != "cuda":
        raise RuntimeError("--require-cuda was passed, but torch.cuda.is_available() is False")

    args.out_dir.mkdir(parents=True, exist_ok=True)
    raw_csv = args.out_dir / "all_fits_summary.csv"
    model_csv = args.out_dir / "running_model_summary.csv"
    param_csv = args.out_dir / "running_param_summary.csv"
    error_csv = args.out_dir / "running_errors.csv"
    txt_path = args.out_dir / "running_summary.txt"
    truth_json = args.out_dir / "truth_params_iso_reference.json"
    config_json = args.out_dir / "run_config.json"

    set_seed(args.seed)
    print("SRC:", SRC, flush=True)
    print("DEVICE:", DEVICE, flush=True)
    print("torch:", torch.__version__, flush=True)
    print("args:", vars(args), flush=True)
    print("NOTE: loading pre-generated simulation assets; no new simulation is run.", flush=True)

    asset_bank, truth = build_asset_bank(args)
    specs = make_specs(args)
    truth_json.write_text(json.dumps(truth, indent=2), encoding="utf-8")
    config_json.write_text(
        json.dumps(
            {
                **{k: str(v) if isinstance(v, Path) else v for k, v in vars(args).items()},
                "src": SRC,
                "device": str(DEVICE),
                "n_assets_loaded": len(asset_bank),
                "n_specs": len(specs),
                "specs": specs,
                "truth_range_convention": "range=sqrt(range_lat*range_lon)",
            },
            indent=2,
        ),
        encoding="utf-8",
    )

    done = existing_completed(raw_csv) if args.resume else set()
    fit_id = 0
    if raw_csv.exists():
        old = pd.read_csv(raw_csv)
        if not old.empty and "fit_id" in old:
            old_fit_ids = pd.to_numeric(old["fit_id"], errors="coerce").dropna()
            if not old_fit_ids.empty:
                fit_id = int(old_fit_ids.max())

    print("truth iso reference:", {k: truth[k] for k in PARAMS}, flush=True)
    print(f"Loaded {len(asset_bank)} day assets; specs={len(specs)}", flush=True)

    for sim_id in range(1, int(args.num_sims) + 1):
        print("\n" + "=" * 100, flush=True)
        print(f"Simulation/day draw {sim_id}/{args.num_sims}", flush=True)
        asset = choose_asset(asset_bank, sim_id, args)
        iter_seed = int(args.seed + sim_id)
        set_seed(iter_seed)
        rng = np.random.default_rng(iter_seed)

        source_map_raw = {
            k: v.to(device=DEVICE, dtype=DTYPE, non_blocking=True).contiguous()
            for k, v in asset["source_map"].items()
        }
        grid_coords_np = np.asarray(asset["grid_coords_np"], dtype=np.float64)
        n_valid = sum(int(torch.isfinite(v[:, 2]).sum().item()) for v in source_map_raw.values())
        print(
            f"asset=year{asset['year']} day_idx={asset['day_idx']} n_grid={asset['n_grid']:,} "
            f"n_valid={n_valid:,} valid={asset['valid_by_t']}",
            flush=True,
        )

        for spec in specs:
            key = (sim_id, spec["spec_name"])
            if args.resume and key in done:
                print(f"Skipping completed sim={sim_id} spec={spec['spec_name']}", flush=True)
                continue

            fit_id += 1
            print(f"\n--- fit_id={fit_id} {spec['spec_name']} ---", flush=True)
            row_base = {
                "fit_id": fit_id,
                "sim_id": sim_id,
                "seed": iter_seed,
                "asset_year": asset["year"],
                "asset_day_idx": asset["day_idx"],
                "asset_first_key": asset["day_keys"][0],
                "data_kind": args.data_kind,
                "n_valid": int(n_valid),
                "n_grid": int(asset["n_grid"]),
                "valid_by_t": json.dumps(asset["valid_by_t"], separators=(",", ":")),
            }
            try:
                row = fit_model(spec, source_map_raw, grid_coords_np, truth, args, rng)
                row.update(row_base)
                compact = {
                    k: (round(v, 5) if isinstance(v, (float, np.floating)) else v)
                    for k, v in row.items()
                    if k in ["spec_name", "loss", "overall_rmsre", "median_abs_error", "total_s", "n_cond_points_nominal"]
                }
                print("RESULT:", compact, flush=True)
            except Exception as exc:
                row = {
                    **row_base,
                    "model_family": spec["model_family"],
                    "spec_name": spec["spec_name"],
                    "smooth": truth.get("smooth", args.smooth_fallback),
                    "mean_design": args.mean_design,
                    "cluster_block_shape": f"{args.cluster_block_shape[0]}x{args.cluster_block_shape[1]}",
                    "cluster_neighbor_blocks": spec.get("cluster_neighbor_blocks", ""),
                    "error": repr(exc),
                }
                print("ERROR:", repr(exc), flush=True)

            append_row_csv(raw_csv, row, ROW_COLUMNS)
            if fit_id % int(args.summary_every) == 0:
                refresh_outputs(raw_csv, model_csv, param_csv, error_csv, txt_path, truth)
            gc.collect()
            if torch.cuda.is_available():
                torch.cuda.empty_cache()

        refresh_outputs(raw_csv, model_csv, param_csv, error_csv, txt_path, truth)

    refresh_outputs(raw_csv, model_csv, param_csv, error_csv, txt_path, truth)
    print("Done.", flush=True)


if __name__ == "__main__":
    main()
